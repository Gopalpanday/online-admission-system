function isBlank()
{	
	var val = document.getElementById('admin_id').value;
	if(val == '')
	{
		document.getElementById('admin_id').focus();
		return false;
	}
	var val1 = document.getElementById('pass').value;
	if(val1 == '')
	{
		document.getElementById('pass').focus();
		return false;
	}
}
